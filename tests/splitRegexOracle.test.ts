import { readFileSync } from "node:fs";
import { gunzipSync } from "node:zlib";
import Split from "../src/core/preTokenizer/Split";

import type { TokenizerConfigPreTokenizerSplit } from "../src/static/tokenizer";

type SplitBehavior = TokenizerConfigPreTokenizerSplit["behavior"];

type SplitRegexPattern = {
  id: string;
  name?: string;
  pattern: string;
  behavior?: SplitBehavior;
  invert?: boolean;
  knownDivergence?: boolean;
  inputs?: string[];
};

type SplitRegexCase = {
  name: string;
  patternId: string;
  behavior?: SplitBehavior;
  invert?: boolean;
  knownDivergence?: boolean;
  inputs?: string[];
};

type PatternsFixture = {
  defaults: { behavior: SplitBehavior; invert: boolean; inputs: string[] };
  patterns: SplitRegexPattern[];
  additionalCases?: SplitRegexCase[];
};

type ResolvedCase = {
  name: string;
  patternId: string;
  pattern: string;
  behavior: SplitBehavior;
  invert: boolean;
  knownDivergence: boolean;
  inputs: string[];
};

const read = (name: string) => readFileSync(new URL(`./fixtures/${name}`, import.meta.url));

const patternsFixture = JSON.parse(read("splitRegexPatterns.json").toString("utf8")) as PatternsFixture;

// The oracle stores only the expected tokens (the one thing not derivable from the input
// fixture), keyed by case name. It is committed gzipped (see
// tests/py/generate_split_regex_oracle.py) to keep a small binary blob in git rather than a
// large generated text file.
const oracle = JSON.parse(gunzipSync(read("splitRegexOracle.json.gz")).toString("utf8")) as { tokenizersVersion: string; tokens: Record<string, string[][]> };

// Mirror of resolve_case/iter_cases in tests/py/generate_split_regex_oracle.py: each case
// inherits behavior/invert/inputs from its pattern, then from the fixture defaults.
const resolveCases = (fx: PatternsFixture): ResolvedCase[] => {
  const byId = new Map(fx.patterns.map((p) => [p.id, p]));
  const resolve = (c: SplitRegexCase): ResolvedCase => {
    const p = byId.get(c.patternId);
    if (!p) throw new Error(`Unknown patternId: ${c.patternId}`);
    return {
      name: c.name,
      patternId: c.patternId,
      pattern: p.pattern,
      behavior: c.behavior ?? p.behavior ?? fx.defaults.behavior,
      invert: c.invert ?? p.invert ?? fx.defaults.invert,
      knownDivergence: c.knownDivergence ?? p.knownDivergence ?? false,
      inputs: c.inputs ?? p.inputs ?? fx.defaults.inputs,
    };
  };
  return [...fx.patterns.map((p) => resolve({ name: p.name ?? p.id, patternId: p.id })), ...(fx.additionalCases ?? []).map(resolve)];
};

const cases = resolveCases(patternsFixture);

const create_split = (c: ResolvedCase) =>
  new Split({
    type: "Split",
    pattern: { Regex: c.pattern },
    behavior: c.behavior,
    invert: c.invert,
  });

describe(`Split regex oracle generated by Python tokenizers ${oracle.tokenizersVersion}`, () => {
  it("oracle has expected tokens for every case (regenerate with tests/py/generate_split_regex_oracle.py)", () => {
    for (const c of cases) {
      expect(oracle.tokens[c.name]).toBeDefined();
      expect(oracle.tokens[c.name]).toHaveLength(c.inputs.length);
    }
  });

  it.each(cases.filter((c) => !c.knownDivergence))("matches Python Split output: $name", (c) => {
    const preTokenizer = create_split(c);
    const expected = oracle.tokens[c.name];
    c.inputs.forEach((input, i) => {
      expect(preTokenizer.pre_tokenize_text(input)).toEqual(expected[i]);
    });
  });

  // These cases pin behavior we knowingly cannot reproduce (see "Regex compatibility" in the
  // README). If one starts matching Python, the divergence has been fixed: clear its
  // `knownDivergence` flag in splitRegexPatterns.json and regenerate the oracle.
  it.each(cases.filter((c) => c.knownDivergence))("documents known divergence from Python: $name", (c) => {
    const preTokenizer = create_split(c);
    const outputs = c.inputs.map((input) => preTokenizer.pre_tokenize_text(input));
    expect(outputs).not.toEqual(oracle.tokens[c.name]);
  });
});
